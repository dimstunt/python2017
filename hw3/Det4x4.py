mat = eval(input())

def det2(x):
	print('det2', x, x[0][0]*x[1][1] - x[0][1]*x[1][0])
	return x[0][0]*x[1][1] - x[0][1]*x[1][0]
def det3(x):
	s1 = x[0][0]*det2(((x[1][1], x[1][2]), (x[2][1], x[2][2])))
	s2 = x[0][1]*det2(((x[1][0], x[1][2]), (x[2][0], x[2][2])))
	s3 = x[0][2]*det2(((x[1][0], x[1][1]), (x[2][0], x[2][1])))
	print('det3', x, s1 - s2 + s3)
	return s1 - s2 + s3
def det4(x):
	s1 = x[0][0]*det3(((x[1][1], x[1][2], x[1][3]), (x[2][1], x[2][2], x[2][3]), (x[3][1], x[3][2], x[3][3])))
	s2 = x[0][1]*det3(((x[1][0], x[1][2], x[1][3]), (x[2][0], x[2][2], x[2][3]), (x[3][0], x[3][2], x[3][3])))
	s3 = x[0][2]*det3(((x[1][0], x[1][1], x[1][3]), (x[2][0], x[2][1], x[2][3]), (x[3][0], x[3][1], x[3][3])))
	s4 = x[0][3]*det3(((x[1][0], x[1][1], x[1][2]), (x[2][0], x[2][1], x[2][2]), (x[3][0], x[3][1], x[3][2])))
	print('det4', x, s1 - s2 + s3 - s4)
	return s1 - s2 + s3 - s4
print(det4(mat))